package com.Looping_Statement_ForLoop;

public class Demo_for_1 {
	public static void main(String[] args) {
		for (int i = 1; i < 11; i++) {
			System.out.println("2*" + i + "=" + (i * 2));

		}
		System.out.println("************************");
		for(int j=1;j<=10;j++) {
			System.out.println("29*"+j+"="+(j*29));
		}
	}

}
