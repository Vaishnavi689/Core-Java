package com.Looping_Statement_ForLoop;

public class Demo_For {
	// If i want to repeat some code on the basis of some condition then we can go for looping
	//Loops in programming allow a set of instructions to run multiple times based on a condition. 
	//In Java, there are 5 types of Loops, which are explained below:
	//1. For loop 2.while loop 3.Do while loop 4.Labled For loop 5.For Each loop
	// For Loop:  for(initialization;condition;updation){ //code}
	//         initialization;   condition;  updation
	// e.g for(int i=1;          i<=10;       i++){ // code}
	
	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			System.out.println("Ram");	
		}
		for(int j=1;j<101;j++) {
			System.out.println(j);
			
		}
		
	}

}
